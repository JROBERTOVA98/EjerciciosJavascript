//switch statements
const grade = 'D';

switch(grade){
    case 'A':
        console.log('tienes una A!');
        break;
    case 'B':
        console.log('tienes una B!');
        break;
    case 'C':
        console.log('tienes una C!');
        break;
    case 'D':
        console.log('tienes una D!');
        break;
    case 'E':
        console.log('tienes una E!');
        break;
    default:
        console.log('no valido');
}